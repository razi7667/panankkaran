const {
    EmbedBuilder,
    ActionRowBuilder,
    ButtonBuilder,
    ButtonStyle,
  } = require('discord.js');
  
  function getStatusEmbedRow(isOnline) {
    const embed = new EmbedBuilder()
      .setAuthor({
        name: 'Panakkaran Bot Status',
        iconURL: 'https://i.postimg.cc/T1PmqhF6/a-PPLICATION.png',
      })
      .setColor(isOnline ? 'Green' : 'Red')
      .addFields({
        name: 'â€¢ Status',
        value: isOnline
          ? '```ðŸŸ¢ Online```\n**â€¢ Uptime:** <t:' + Math.floor(Date.now() / 1000) + ':R>\n\u200B'
          : '```ðŸŸ¥ Offline```\n**â€¢ Uptime:** 0 seconds\n\u200B',
      })
      .setFooter({ text: 'Powered by Muhammed Raziâ„¢' })
      .setTimestamp();
  
    const row = new ActionRowBuilder().addComponents(
      new ButtonBuilder()
        .setCustomId('wake_bot')
        .setLabel('ðŸ”„ Wake')
        .setStyle(ButtonStyle.Success)
        .setDisabled(isOnline),
  
      new ButtonBuilder()
        .setCustomId('sleep_bot')
        .setLabel('ðŸ˜´ Sleep')
        .setStyle(ButtonStyle.Danger)
        .setDisabled(!isOnline)
    );
  
    return { embed, row };
  }
  
  async function sendStatusEmbed(channel, isOnline) {
    const { embed, row } = getStatusEmbedRow(isOnline);
    return await channel.send({ embeds: [embed], components: [row] });
  }
  
  async function editStatusEmbed(message, isOnline) {
    const { embed, row } = getStatusEmbedRow(isOnline);
    return await message.edit({ embeds: [embed], components: [row] });
  }
  
  module.exports = {
    sendStatusEmbed,
    editStatusEmbed,
  };
  